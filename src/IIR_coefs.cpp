enum E_filter_type
{
  E_FT_LPF, E_FT_HPF
};

/*! Highpass filter IIR design
 *
 * @param sampling_rate
 * @param Nb - liczba współczynników licznika transmitancji
 * @param b
 * @param Na - liczba współczynników mianownika transmitancji
 * @param a
 * @return
 *
 * \warning user must delete vectors allocated in a and  b variables
 */
bool GetIIR_coefs(E_filter_type filter_type, float Fp, long sampling_rate, int &Nb, DSP_float *&b, int &Na, DSP_float *&a)
{
  int poly_order;
  DSP_prec_float fp;
  DSP_prec_float_ptr *coefs_b, *coefs_a;
  DSP_prec_float *tmp_b, *tmp_a;
  int p, ind;
  int approximation_Fp, minimum_allowed_Fp, maximum_allowed_Fp;
  float Freq_factor;
  T_IIR_coefs *coefs;

  coefs = NULL;
  // ----------------------------------- //
  switch (sampling_rate)
  {
    case 8000:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_8000;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_8000;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 11025:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_11025;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_11025;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 16000:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_16000;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_16000;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 22050:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_22050;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_22050;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 32000:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_32000;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_32000;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 44100:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_44100;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_44100;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;
    case 48000:
      switch (filter_type)
      {
        case E_FT_HPF:
          coefs = new T_HPF_IIR_coefs_48000;
          break;
        case E_FT_LPF:
          coefs = new T_LPF_IIR_coefs_48000;
          break;
        default:
          DSPf_ErrorMessage("GetIIR_coefs", "unsupported filter type");
      }
      break;

    default:
      DSPf_ErrorMessage("GetIIR_coefs", "unsupported sampling rate");
  }


  if (coefs == NULL)
  {
    coefs = NULL;
    Nb = 0; Na = 0;
    b = NULL; a = NULL;
    return false;
  }

  // ----------------------------------- //
  Nb = coefs->filter_order+1;
  Na = coefs->filter_order+1;
  poly_order = coefs->polynomial_order;


  approximation_Fp = coefs->approximation_Fp;
  minimum_allowed_Fp = coefs->minimum_allowed_Fp;
  maximum_allowed_Fp = coefs->maximum_allowed_Fp;
  Freq_factor = coefs->Freq_factor;

  b = new DSP_float[Nb];
  a = new DSP_float[Na];
  if (Fp < minimum_allowed_Fp)
  {
    //Fp = minimum_allowed_Fp;
    Fp = 0;

    for (ind = 0; ind < Nb; ind++)
      b[ind] = 0.0;
    for (ind = 0; ind < Na; ind++)
      a[ind] = 0.0;
    switch (filter_type)
    {
      case E_FT_HPF:
        // allpass filter
        b[0] = 1.0; a[0] = 1.0;
        break;
      case E_FT_LPF:
        // allstop filter
        b[0] = 0.0; a[0] = 1.0;
        break;
      default:
        // allstop filter
        b[0] = 0.0; a[0] = 1.0;
        break;
    }

    #ifdef __DEBUG__
      DSPf_ErrorMessage("GetIIR_HPF_coefs", "Fp < minimum_allowed_Fp");
    #endif
    return false;
  }
  if (Fp > maximum_allowed_Fp)
  {
    //Fp = maximum_allowed_Fp;
    Fp = sampling_rate/2;

    for (ind = 0; ind < Nb; ind++)
      b[ind] = 0.0;
    for (ind = 0; ind < Na; ind++)
      a[ind] = 0.0;
    switch (filter_type)
    {
      case E_FT_HPF:
        // allstop filter
        b[0] = 0.0; a[0] = 1.0;
        break;
      case E_FT_LPF:
        // allpass filter
        b[0] = 1.0; a[0] = 1.0;
        break;
      default:
        // allstop filter
        b[0] = 0.0; a[0] = 1.0;
        break;
    }

    #ifdef __DEBUG__
      DSPf_ErrorMessage("GetIIR_HPF_coefs", "Fp > maximum_allowed_Fp");
    #endif
    return false;
  }

  coefs_b = new DSP_prec_float_ptr[Nb];
  for (p = 0; p < Nb; p++)
    coefs_b[p] = coefs->coef_b[p];
  coefs_a = new DSP_prec_float_ptr[Na];
  for (p = 0; p < Na; p++)
    coefs_a[p] = coefs->coef_a[p];
  // ----------------------------------- //
  delete coefs; coefs = NULL;
  // ----------------------------------- //

  //(f_all(:)-Fs_max/2)*freq_factor/SamplingRate
  fp = (Fp-approximation_Fp/2)*Freq_factor/sampling_rate;

  // compute nominator coefficients
  tmp_b = new DSP_prec_float[Nb];
  for (ind = 0; ind < Nb; ind++)
  {
    tmp_b[ind] = coefs_b[ind][0];
    for (p = 1; p <= poly_order; p++)
    {
      tmp_b[ind] *= fp;
      tmp_b[ind] += coefs_b[ind][p];
    }
    b[ind] = tmp_b[ind];
  }
  delete [] tmp_b;

  // compute denominator coefficients
  tmp_a = new DSP_prec_float[Na];
  for (ind = 0; ind < Na; ind++)
  {
    tmp_a[ind] = coefs_a[ind][0];
    for (p = 1; p <= poly_order; p++)
    {
      tmp_a[ind] *= fp;
      tmp_a[ind] += coefs_a[ind][p];
    }
    a[ind] = tmp_a[ind];
  }
  delete [] tmp_a;

  delete [] coefs_b;
  delete [] coefs_a;
  return true;
}

bool GetIIR_HPF_coefs(float Fp, long sampling_rate, int &Nb, DSP_float *&b, int &Na, DSP_float *&a)
{
  return GetIIR_coefs(E_FT_HPF, Fp, sampling_rate, Nb, b, Na, a);
}
bool GetIIR_LPF_coefs(float Fp, long sampling_rate, int &Nb, DSP_float *&b, int &Na, DSP_float *&a)
{
  return GetIIR_coefs(E_FT_LPF, Fp, sampling_rate, Nb, b, Na, a);
}


